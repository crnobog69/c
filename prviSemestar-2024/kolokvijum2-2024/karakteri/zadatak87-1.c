// Саставити програм који врши конверзију унетих малих слова у велика.
// Унос текста се завршава сигналом ЕОF.

// Први начин (тежи):

#include <stdio.h>

int main()
{
    int c;
    while ((c = getchar()) != EOF)
    {
        if (c >= 'a' && c <= 'z')
            c = c - 'a' + 'A';
        putchar(c);
    }

    getchar();
    return 0;
}