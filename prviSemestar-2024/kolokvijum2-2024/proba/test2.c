// Задатак 89:
// Саставити програм који копира улазни текст на екран, где сваку реч приказује у једној линији.
// Унос текста се завршава сигналом ЕОF.

#include <stdio.h>

int main()
{
    int c;
    while ((c = getchar()) != EOF)
    {
        if (c == ' ' || c == '\n' || c == '\t')
        {
            putchar('\n');
        }
        else
        {
            putchar(' ');
        }
    }

    getchar();
    return 0;
}