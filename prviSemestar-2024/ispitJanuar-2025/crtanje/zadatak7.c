/*
Програм црта троугао од звездица. Ево како ради:

1. Први део прави троугао који расте надоле:
   - На почетку сваког реда ставимо потребан број размака
   - Затим штампамо звездице са размаком "* "
   - У задњој звездици не треба размак па њу штампамо посебно

   На пример, за n = 3:
      *      (2 размака, па само *)
     * *     (1 размак, па "* " и на крају *)
    * * *    (без размака, па "* * " и на крају *)

2. Други део прави троугао који се смањује:
   - Исти принцип као горе, само идемо уназад
   - Крећемо од n-2 (претпоследњи ред)
*/

int main()
{
    int i, j, n;

    printf("n = ");
    scanf("%d", &n);

    printf("\n");

    // Правимо троугао који расте надоле
    for (i = 0; i < n; i++)
    {
        // Прво ставимо празна места
        for (j = 0; j < n - i - 1; j++)
        {
            printf(" ");
        }
        // Сад цртамо звездице са размаком
        for (j = 0; j < i; j++)
        {
            printf("* ");
        }
        printf("*\n"); // Последња звездица без размака
    }

    // Правимо троугао који се смањује надоле
    for (i = n - 2; i >= 0; i--)
    {
        // Прво ставимо празна места
        for (j = 0; j < n - i - 1; j++)
        {
            printf(" ");
        }
        // Сад цртамо звездице са размаком
        for (j = 0; j < i; j++)
        {
            printf("* ");
        }
        printf("*\n"); // Последња звездица без размака
    }

    getchar();
    return 0;
}