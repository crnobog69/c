#include <stdio.h> // Укључујемо библиотеку за коришћење функција за испис на екран

// Почетак главне функције
int main()
{
    int a, b, c; // Декларишемо три цела броја: 'a', 'b' и 'c'

    // Прва петља - 'a' почиње од 1 и иде до 9
    for (a = 1; a <= 9; a++)
    {
        // Друга петља - 'b' почиње од 3 и иде до 9
        for (b = 3; b <= 9; b++)
        {
            // Трећа петља - 'c' почиње од 4 и иде до 9
            for (c = 4; c <= 9; c++)
            {
                // Проверавамо да ли је 'b' два већа од 'a' (b = a + 2)
                // и да ли је 'c' један већи од 'b' (c = b + 1)
                if (b == a + 2 && c == b + 1)
                {
                    // Исписујемо комбинацију 'a', 'b' и 'c'
                    printf("%d%d%d\n", a, b, c);
                }
            }
        }
    }

    return 0; // Враћамо 0, што значи да је програм успешно завршио
}

// Пример објашњења за прву комбинацију:
